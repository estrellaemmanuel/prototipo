    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if(!tabla.getSelectionModel().isSelectionEmpty()){
        int row = tabla.getSelectedRow();
        int Onclick = (int) (tabla.getModel().getValueAt(row, 0)) ; 
        String ProductoNombre = (tabla.getModel().getValueAt(row, 1).toString()) ; 
        int reply = JOptionPane.showConfirmDialog(null, "Realmente desea eliminar el Empleado ID " + Onclick +" - "+ProductoNombre+" ?", "Confirmar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if (reply == JOptionPane.YES_OPTION) {
            gg.EliminarEmpleado(Onclick);
            JOptionPane.showMessageDialog(null, "El Empleado fue eliminado correctamente");
            LimpiarTabla();
            actualizarTabla(DBSetencias.ConsultaSelectEmpleado);
          
        }else {
           JOptionPane.showMessageDialog(null, "Se cancelo la operacion para eliminar el Empleado");
        }
         }else{
               JOptionPane.showMessageDialog(null, "Para eliminar un Empleado seleccione uno de la lista"); 
         
          }
         
        
    }//GEN-LAST:event_jButton4ActionPerformed

