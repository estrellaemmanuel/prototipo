    public void LimpiarTabla(){
         int rowCount = modelo.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
          modelo.removeRow(i);
          }
        actualizarTabla(DBSetencias.ConsultaSelectEmpleado);
    }

